    public void deleteClient() {
        try {
            int row = tableClients.getSelectedRow();

            if (row == -1) {
                JOptionPane.showMessageDialog(null, "Select a Client to Delete.");
                return;
            }

            String id = (String) tableClients.getValueAt(row, 0);
            int response = JOptionPane.showConfirmDialog(this, "Delete Client Code - " + id + " ?", "Confirm...", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response == JOptionPane.YES_OPTION) {
                String client_code = (String) tableClients.getValueAt(row, 0);

                String queryDeleteClient = "DELETE FROM clientinformation WHERE Client_Code=?";
                pst = con.prepareStatement(queryDeleteClient);
                pst.setString(1, client_code);
                pst.executeUpdate();

                FetchClient();
                loadClient_ID();
                displayTCountClients();
                JOptionPane.showMessageDialog(null, "Deleted Successfully!");    
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "This Client still has a Billing Record. It cannot be Deleted!");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "Out of Range!");
        }
    }
    
