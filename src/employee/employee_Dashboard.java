                String user_status = rs.getString(8);
                
                String name = lname + ", " + fname + " " + mname;
                                
                for(int i = 1; i <= q; i++) {
                    rows.add(id);
                    rows.add(date);
                    rows.add(name);
                    rows.add(username);
                    rows.add(user_type);
                    rows.add(user_status);
                }
                tblUsers.addRow(rows);
